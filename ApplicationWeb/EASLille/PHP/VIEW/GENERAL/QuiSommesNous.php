      <div> <br> <br />
        <br />

       <P>Le but de notre structure s’inscrit dans un cadre de dialogue, d’échanges et de dynamisme dans la synergie d’action.<br>
        Les Principales activités de l’Association des étudiants africains du Nord Pas de Calais.<br>
        1- Créer un lien de solidarité entre les étudiants ;<br>
        2- Mise en place d’une structure d’information et d’accueil des nouveaux étudiants ;<br>
        3- Mise en place des cours de tutorats pour les étudiants en difficulté ;<br>
        4- Organisation des manifestations sportives, culturelles et artistiques ;<br>
        5- Mise en place d’un fond d’aide et de solidarité ;<br>
        6- Coopération avec les autres associations.<br>
        Notre objectif, contribué à l’épanouissement de nos membres par un engament commun qui fait un succès général.<br><br />
