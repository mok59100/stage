    <legend> NOTRE ETHIQUE </legend>
    <div class="beautiful"><br>
   
      – Travail<br>

      Nous réalisons nos ambitions par le travail, à travers lui nous façonnons notre environnement en l’imprimant notre idéal.<br> C’est un levier important par lequel nos chances se multiplient et nos rêves se concrétisent.<br> Nous œuvrons permanemment pour cultiver l’amour du travail bien accompli.<br>

      – Solidarité<br>

      Nous estimons que Tout groupe humain prend sa richesse dans la communication, l’entraide et la solidarité visant à un but commun : <br> l’épanouissement de chacun dans le respect des différences.<br> Nous enseignons à chacun de nos membres, la compréhension entre les humains qui est la condition et le garant de la solidarité intellectuelle et morale.<br>

      – Transparence<br>

      Notre credo réside dans notre capacité de gestion en toute sincérité. <br>L’opacité sous toutes ses formes est bannie de notre mode de gestion.<br> Nous estimons que notre intégrité, notre probité morale et notre crédibilité y dépendent tant pour nos élus que pour nos collaborateurs.<br> S’investir pour plus de transparence est de mise au sein de notre association en général et du bureau exécutif en particulier.<br> Le laxisme est notre ennemi commun pour toujours réussir ce pari.<br>
