        Solidariser, nouer l'entraide intercommunautaire,
        rassembler les membres de la diaspora est africaine autour de la pratique hebdomadaire du sport.<br>
        Organiser des evenements pour partager et promouvoir les cultures et moeurs Est africaines( evenement sportif, exposition, concert,etc..).<br>
        Soutenir l'emergence des projets visant la création d'activité locale et d'emploi dans les pays de l'afrique de l'est, pour et avec la populations locale.<br>
        Transfert et echanges des technologies.<br>
        Faciliter l'intégration des membres de la communaute est africaine dans la region lilloise.<br>
        Pour faire parti de cette associaion , il faut adherer et s'acquiter de la cotisation.<br> Les mineurs peuvent adherer à l'association sous reserve d'un accord de leurs
        parents ou tuteurs legaux.Ils sont membres à part entiere de l'association.<br>
        L'association s'interdit toute discrimination, veille au respect de ce principe et garantit la liberté de conscience pour chacun de ses membres.<br>

        Le siege social est fixe à la maison des association au 27, rue jean Bart 59000lille. <br>
        Les ressources de l'associations comprennent les dons , subventions ainsi que les cotisations.<br>
        Les recettes des ventes de prestation de service et de bien , en relation avec les activites de l'association. <br>
        Nous œuvrons pour le succès des étudiants en Nord-Pas-de-Calais et pour leur épanouissement de la façon suivante :<br>

        Créer un lien de solidarité entre les étudiants .<br>
        Mettre en place une structure d’accueil et d’accompagnement des nouveaux étudiants.<br>
        Mettre en place des cours de tutorat pour les étudiants en difficulté .<br>
        Élaboration d’une cellule d’information pour les stages et jobs d’étudiants .<br>
        Organisation des manifestations sportives, culturelles et artistiques .<br>
        Mettre en place un fonds d’aide et de solidarité .<br>
        Coopération avec les autres associations .<br>
        Œuvrer pour un partenariat entre le Nord Pas-de-Calais et l'afrique de l'est.<br>
